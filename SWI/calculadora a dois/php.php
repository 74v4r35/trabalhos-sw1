<?php

    class operador{

       public function somar($num1, $num2){
        return $num1+$num2;
       }
       public function sub($num1, $num2){
        return $num1-$num2;
       }
       public function mult($num1, $num2){
        return $num1*$num2;
       }
       public function div($num1, $num2){
        return $num1/$num2;
       }
    }

?>
